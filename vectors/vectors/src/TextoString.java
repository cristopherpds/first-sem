public class TextoString {
    public static void main(String[] args) throws Exception {
        String texto = "IFSUL - TADS";
        String frase = texto + "Brasil e Uruguai";
        char[] array = { 'J', 'A', 'V', 'A' };
        String vetor = new String(array);

        System.out.println("Vetor: " + vetor);

        System.out.println("Texo: " + texto);
        System.out.println("Frase: " + frase.length());
        
    }
}
